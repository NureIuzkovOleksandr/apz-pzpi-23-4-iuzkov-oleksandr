export function t(locale: LocaleCode, key: string): string {
  return translations[locale][key] ?? translations.en[key] ?? key;
}

export function getDirection(locale: LocaleCode) {
  return directions[locale] ?? 'ltr';
}

export type DatePreset = 'short' | 'medium' | 'long';

function getStoredDatePreset(): DatePreset {
  try {
    const raw = localStorage.getItem('web_app_settings');
    if (!raw) return 'medium';
    const parsed = JSON.parse(raw);
    return (parsed.dateFormat as DatePreset) ?? 'medium';
  } catch {
    return 'medium';
  }
}

export function formatDate(locale: LocaleCode, dateInput: string | Date): string {
  const date = typeof dateInput === 'string' ? new Date(dateInput) : dateInput;
  const preset = getStoredDatePreset();
  let options: Intl.DateTimeFormatOptions;
  switch (preset) {
    case 'short':
      options = { dateStyle: 'short', timeStyle: 'short' };
      break;
    case 'long':
      options = { dateStyle: 'long', timeStyle: 'short' };
      break;
    case 'medium':
    default:
      options = { dateStyle: 'medium', timeStyle: 'short' };
  }
  try {
    return new Intl.DateTimeFormat(locale, options).format(date);
  } catch {
    return date.toLocaleString();
  }
}

